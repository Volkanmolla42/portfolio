/**
 * Get a tech icon by name
 */
export function getTechIconByName(name: string): TechIcon | undefined {
  return techIcons.find(
    (icon) => icon.name.toLowerCase() === name.toLowerCase()
  );
}

/**
 * Get multiple tech icons by names
 */
export function getTechIconsByNames(names: string[]): TechIcon[] {
  return names
    .map((name) => getTechIconByName(name))
    .filter((icon): icon is TechIcon => icon !== undefined);
}